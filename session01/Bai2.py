# Bài 2: Khai báo biến với các kiểu dữ liệu khác nhau

# Khai báo 4 biến với 4 kiểu dữ liệu khác nhau
so_nguyen = 25                    # Kiểu int (số nguyên)
so_thuc = 3.14                    # Kiểu float (số thực)
chuoi_ky_tu = "Hello Python"      # Kiểu str (chuỗi ký tự)
gia_tri_logic = True              # Kiểu bool (giá trị logic)

# In ra giá trị và kiểu dữ liệu của từng biến
print("Biến so_nguyen:")
print("Giá trị:", so_nguyen)
print("Kiểu dữ liệu:", type(so_nguyen))
print()

print("Biến so_thuc:")
print("Giá trị:", so_thuc)
print("Kiểu dữ liệu:", type(so_thuc))
print()

print("Biến chuoi_ky_tu:")
print("Giá trị:", chuoi_ky_tu)
print("Kiểu dữ liệu:", type(chuoi_ky_tu))
print()

print("Biến gia_tri_logic:")
print("Giá trị:", gia_tri_logic)
print("Kiểu dữ liệu:", type(gia_tri_logic))
